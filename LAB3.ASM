.MODEL SMALL
.DATA

N DB 5
A DB 05,07,04,03,06

.CODE

MOV AX, @DATA
MOV DS, AX

LEA SI, A
MOV CL, N
DEC CL
OUTLOOP: MOV CH, CL
MOV SI, 00H
INLOOP: MOV AL, A[SI]
JC NOEXCHANGE
XCHG AL, A[SI]
MOV A[SI-1], AL
NOEXCHANGE: DEC CH
JNZ INLOOP
DEC CL
JNZ OUTLOOP

MOV AH, 4CH
INT 21H
END
